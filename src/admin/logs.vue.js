const getUserName = (userId) => {
    if (!userId)
        return null;
    const user = adminStore.users.find((u) => u.id === userId);
    return user ? user.name : null;
};
const getDateRangeStart = () => {
    const now = new Date();
    switch (dateRange.value) {
        case 'today':
            return new Date(now.setHours(0, 0, 0, 0));
        case 'yesterday':
            return subDays(new Date(now.setHours(0, 0, 0, 0)), 1);
        case 'week':
            return subDays(now, 7);
        case 'month':
            return subDays(now, 30);
        default:
            return new Date(0); // beginning of time
    }
};
const filteredLogs = computed(() => {
    let filtered = adminStore.systemLogs;
    // Apply log level filter
    if (logLevel.value !== 'all') {
        filtered = filtered.filter(log => log.level === logLevel.value);
    }
    // Apply date range filter
    if (dateRange.value !== 'all') {
        const startDate = getDateRangeStart();
        filtered = filtered.filter(log => new Date(log.timestamp) >= startDate);
    }
    // Apply search filter
    if (searchQuery.value) {
        const query = searchQuery.value.toLowerCase();
        filtered = filtered.filter(log => log.message.toLowerCase().includes(query) ||
            (log.userId && getUserName(log.userId)?.toLowerCase().includes(query)));
    }
    return filtered;
});
